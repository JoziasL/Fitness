        <h3>Exercise 2: Bent-Over Rows 4x10</h3>
        <p>Instructions:</p>
        <ul>
            <li>Stand with your feet shoulder-width apart, knees slightly bent, and bend forward at the hips.</li>
            <li>Grasp a barbell with an overhand grip, hands slightly wider than shoulder-width apart.</li>
            <li>Keep your back straight and your core engaged as you pull the barbell towards your lower chest, squeezing your shoulder blades together.</li>
            <li>Lower the barbell back down in a controlled manner and repeat for the desired number of repetitions.</li>
        </ul>
