        <h3>Exercise 4: Lat Pulldowns 4x10</h3>
        <p>Instructions:</p>
        <ul>
            <li>Sit at a lat pulldown machine with your thighs secured under the pads.</li>
            <li>Grasp the bar with an overhand grip, hands wider than shoulder-width apart.</li>
            <li>Pull the bar down towards your upper chest, focusing on engaging your lat muscles.</li>
            <li>Slowly release the bar back up to the starting position and repeat for the desired number of repetitions.</li>
        </ul>
    </div>

