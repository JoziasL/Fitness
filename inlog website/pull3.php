        <h3>Exercise 3: One-Arm Dumbbell Rows 4x12</h3>
        <p>Instructions:</p>
        <ul>
            <li>Place one knee and hand on a bench, keeping your back parallel to the ground.</li>
            <li>Hold a dumbbell in your opposite hand with your arm fully extended.</li>
            <li>Pull the dumbbell up towards your waist, squeezing your back muscles at the top of the movement.</li>
            <li>Lower the dumbbell back down in a controlled manner and repeat for the desired number of repetitions.</li>
        </ul>
    </div>

