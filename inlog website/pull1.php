    <div class="exercise">
        <img src="../images/img_16.png" alt="Squats" class="exercise-image">
        <h3>Exercise 1: Lat Pulldown 4x10</h3>
        <p>Instructions:</p>
        <ul>
            <li>Sit down on a lat pulldown machine and adjust the thigh pads to a comfortable position.</li>
            <li>Grasp the bar with an overhand grip, hands slightly wider than shoulder-width apart.</li>
            <li>Lean back slightly and pull the bar down towards your chest, bringing your elbows towards the floor.</li>
            <li>Keep your torso stationary and squeeze your back muscles at the bottom of the movement.</li>
            <li>Slowly release the bar back to the starting position and repeat for the desired number of repetitions.</li>
        </ul>
    </div>

    <div class="exercise">
        <img src="../images/img_17.png" alt="Bench Press" class="exercise-image">
        <h3>Exercise 2: Dumbbell Rows 4x10</h3>
        <p>Instructions:</p>
        <ul>
            <li>Place one knee and one hand on a flat bench, ensuring your back is parallel to the ground.</li>
            <li>Hold a dumbbell in your opposite hand with your arm fully extended towards the ground.</li>
            <li>Pull the dumbbell up towards your hip, keeping your elbow close to your body and squeezing your back muscles.</li>
            <li>Lower the dumbbell back down to the starting position in a controlled manner and repeat for the desired number of repetitions.</li>
            <li>Switch sides and repeat the exercise.</li>
        </ul>
    </div>

    <div class="exercise">
        <img src="../images/img_18.png" alt="Bench Press" class="exercise-image">
        <h3>Exercise 3: Assisted Pull-Ups 3x10</h3>
        <p>Instructions:</p>
        <ul>
            <li>Position yourself on an assisted pull-up machine, adjusting the weight to an appropriate level.</li>
            <li>Grasp the handles with an overhand grip, hands shoulder-width apart.</li>
            <li>Initiate the pull-up motion by engaging your back muscles and pulling your body upward.</li>
            <li>Continue pulling until your chin is above the bar, maintaining a controlled movement throughout.</li>
            <li>Slowly lower your body back down to the starting position and repeat for the desired number of repetitions.</li>
        </ul>
    </div>

    <div class="exercise">
        <img src="../images/img_19.png" alt="Bench Press" class="exercise-image">
        <h3>Exercise 4: Barbell Deadlifts 4x10</h3>
        <p>Instructions:</p>
        <ul>
            <li>Stand with your feet hip-width apart, toes pointing forward, and a barbell in front of you.</li>
            <li>Bend your knees and hinge at the hips to grip the barbell with an overhand grip, hands shoulder-width apart.</li>
            <li>Keep your back straight, chest lifted, and engage your core.</li>
            <li>Drive through your heels, extend your hips, and stand up straight while lifting the barbell.</li>
            <li>Lower the barbell back down to the starting position by bending your knees and hinging at the hips.</li>
        </ul>
    </div>

    <div class="exercise">
        <img src="../images/img_20.png" alt="Bench Press" class="exercise-image">
        <h3>Exercise 5: Bicep Curls 3x10</h3>
        <p>Instructions:</p>
        <ul>
            <li>Stand up straight with a dumbbell in each hand, arms extended by your sides.</li>
            <li>Keep your elbows close to your torso and rotate your palms to face forward.</li>
            <li>Keeping your upper arms stationary, exhale and curl the weights while contracting your biceps.</li>
            <li>Continue to raise the dumbbells until your biceps are fully contracted and the dumbbells are at shoulder level.</li>
            <li>Slowly lower the dumbbells back to the starting position and repeat for the desired number of repetitions.</li>
        </ul>
    </div>

    <div class="exercise">
        <img src="../images/img_21.png" alt="Bench Press" class="exercise-image">
        <h3>Exercise 6: Hammer Curls 4x10</h3>
        <p>Instructions:</p>
        <ul>
            <li>Stand up straight with a dumbbell in each hand, arms extended by your sides.</li>
            <li>Keep your elbows close to your torso and rotate your palms to face your body (hammer grip).</li>
            <li>Keeping your upper arms stationary, exhale and curl the weights while contracting your biceps.</li>
            <li>Continue to raise the dumbbells until your biceps are fully contracted and the dumbbells are at shoulder level.</li>
            <li>Slowly lower the dumbbells back to the starting position and repeat for the desired number of repetitions.</li>
        </ul>
