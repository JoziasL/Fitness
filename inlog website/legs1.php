    <div class="exercise">
        <img src="../images/img_11.png" alt="Bench Press" class="exercise-image">
        <h3>Exercise 2: Lunges 4x10 (each leg)</h3>
        <p>Instructions:</p>
        <ul>
            <li>Stand with your feet hip-width apart, hands on your hips or at your sides.</li>
            <li>Step forward with your right leg and lower your body until your right knee is bent at a 90-degree angle.</li>
            <li>Keep your torso upright and your front knee in line with your ankle.</li>
            <li>Push through your right heel to return to the starting position and repeat on the left leg.</li>
            <li>Alternate legs for the desired number of repetitions.</li>
        </ul>
    </div>

    <div class="exercise">
        <img src="../images/img_7.png" alt="Bench Press" class="exercise-image">
        <h3>Exercise 3: Romanian Deadlifts 3x10</h3>
        <p>Instructions:</p>
        <ul>
            <li>Stand with your feet hip-width apart, holding a barbell or dumbbells in front of your thighs.</li>
            <li>Engage your core and hinge at the hips, pushing your hips back and lowering the weight along your legs.</li>
            <li>Keep your back straight and a slight bend in your knees throughout the movement.</li>
            <li>Lower the weight until you feel a stretch in your hamstrings, then return to the starting position by driving your hips forward.</li>
        </ul>
    </div>

    <div class="exercise">
        <img src="../images/img_10.png" alt="Bench Press" class="exercise-image">
        <h3>Exercise 4: Calf Raises 4x10</h3>
        <p>Instructions:</p>
        <ul>
            <li>Stand with the balls of your feet on an elevated surface, such as a step or platform.</li>
            <li>Hold onto a wall or stable surface for balance if needed.</li>
            <li>Raise your heels as high as possible by standing on your toes.</li>
            <li>Pause for a moment at the top, then lower your heels back down below the elevated surface.</li>
        </ul>
    </div>

    <div class="exercise">
        <img src="../images/img_9.png" alt="Bench Press" class="exercise-image">
        <h3>Exercise 5: Leg Press 3x10</h3>
        <p>Instructions:</p>
        <ul>
            <li>Sit on a leg press machine with your feet shoulder-width apart on the footplate.</li>
            <li>Press the footplate away from your body by extending your legs, keeping your back against the pad.</li>
            <li>Lower the footplate back down to the starting position by bending your knees, allowing them to come toward your chest.</li>
            <li>Repeat for the desired number of repetitions.</li>
        </ul>
    </div>

    <div class="exercise">
        <img src="../images/img_12.png" alt="Bench Press" class="exercise-image">
        <h3>Exercise 6: Glute Bridge 4x10</h3>
        <p>Instructions:</p>
        <ul>
            <li>Lie on your back with your knees bent and feet flat on the ground, hip-width apart.</li>
            <li>Engage your core and squeeze your glutes to lift your hips off the ground, creating a straight line from your knees to your shoulders.</li>
            <li>Hold for a moment at the top, then lower your hips back down to the starting position.</li>
        </ul>
