        <h3>Exercise 1: Squats 4x10</h3>
        <p>Instructions:</p>
        <ul>
            <li>Stand with your feet shoulder-width apart, toes pointing slightly outward.</li>
            <li>Engage your core, keep your chest lifted, and lower your body by bending your knees and pushing your hips back.</li>
            <li>Lower until your thighs are parallel to the ground, keeping your knees in line with your toes.</li>
            <li>Push through your heels to stand back up to the starting position and repeat for the desired number of repetitions.</li>
        </ul>
